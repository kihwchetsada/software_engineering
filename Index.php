                <?php
                // กำหนดข้อมูลกิจกรรม พร้อม dependencies (กิจกรรมที่ต้องทำก่อน)
                $activities_data = [
                    'A' => ['dependencies' => ['']],
                    'B' => ['dependencies' => ['A']],
                    'C' => ['dependencies' => ['A']],
                    'D' => ['dependencies' => ['C']],
                    'E' => ['dependencies' => ['B']],
